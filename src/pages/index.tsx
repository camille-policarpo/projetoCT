import { FormEvent, useState } from 'react'
import type { NextPage } from 'next'
import Head from 'next/head'
import { Flex, Box, Text, Input, Button, Image } from '@chakra-ui/react';
import { Coordenadas, Openmeteo } from '../services/axios';
import { FaTemperatureHigh } from 'react-icons/fa'
import { TbTemperatureMinus , TbTemperaturePlus } from 'react-icons/tb'
import { MdOutlineWaterDrop } from 'react-icons/md'

interface Coordenada {
  latitude: number;
  longitude: number;
}

const Home: NextPage = () => {

  const [coordenadas, setCoordenada] = useState<Coordenada | null>();
  const [texto, setTexto] = useState('');
  const [temp, setTemp] = useState('');
  const [tMin, setTMin] = useState('');
  const [tMax, setTMax] = useState('');
  const [umi, setUmi] = useState('');

  async function onSubmit(event: FormEvent){
    event.preventDefault();
    const res = await Coordenadas.get(`search?name=${texto}&count=1`)
    const {latitude,longitude,timezone} = res.data.results[0]
    let newTimezone = timezone.replace ('/', '%2F');

    

  
    const api = await Openmeteo.get(`forecast?latitude=${latitude}&longitude=${longitude}&hourly=temperature_2m,relativehumidity_2m&daily=temperature_2m_max,temperature_2m_min&timezone=${newTimezone}`) .then((data) => {
       setTMax(data.data.daily.temperature_2m_max[0])
       setTMin (data.data.daily.temperature_2m_min[0])
       setUmi (data.data.hourly.relativehumidity_2m[0])
       setTemp (data.data.hourly.temperature_2m[0])
   }
  )

  }

  
  


  return (
    <div>
      <Head>
        <title>Consulte o tempo</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
          <Flex as="form" alignItems={"center"} h='100vh' justifyContent='center' bg='blue.100' onSubmit={(event) => onSubmit(event)}>
            <Flex borderRadius="xl" bgImage={'ceu.jpg'} padding={"10"} direction={'column'}> 
            <Input onChange={(event) => setTexto(event.target.value)}/>
            <Button bgColor={'white.400'} type="submit">ENVIAR</Button>

            <Flex alignItems='start' direction={'column'}>
              <FaTemperatureHigh />   <Text noOfLines={1} color={''}> Temperatura °C: {temp}</Text>
              <MdOutlineWaterDrop />  <Text  noOfLines={1} color={''}> Umidade: {umi} </Text>
              <TbTemperaturePlus />   <Text color={''}> Temp máxima do dia: {tMax}</Text>
              <TbTemperatureMinus />  <Text color={''}> Temp mínima do dia: {tMin}</Text>
            </Flex>

            </Flex>
          </Flex>

      </main>
    </div>
  )
}

export default Home